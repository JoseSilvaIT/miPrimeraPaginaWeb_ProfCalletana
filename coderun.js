// Este codigo es para ver si un Numero es Par en una cuenta de numeros
var mondongo = 0;
var CantidadNum = 30
while (mondongo <= CantidadNum) {
    if (mondongo % 2 == 0) {
        console.log(mondongo + " es par");
    }
    mondongo++
}